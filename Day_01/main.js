// Data Types
/* 1. Primitive Data Types
  - String
  - Number
  - Boolean
  - Undefined
  - Null
  - Symbol
*/

// Strings
console.log('single quote');
console.log('double quotes');
console.log(`backtick`);

// Numbers
console.log('-3, -2, -1, 0, 1, 2, 3, ...'); // Intergers
console.log('-3.1, -2.2, -1.3, 0, 1.4, 2.5, 3.6, ...'); // Float-point Numbers

// Booleans
console.log('True'); // If light is on, the value is true
console.log('False'); // If light is off, the value is false

// Undefined
let firstname; // no value assigned
console.log(firstname); // undefined

// Null
let emptyValue = null;
console.log(emptyValue);

// Checking Data Types - use typeof operator
console.log(typeof 'double quote');
console.log(typeof 7);
console.log(typeof true);
console.log(typeof null);
console.log(typeof undefined);
